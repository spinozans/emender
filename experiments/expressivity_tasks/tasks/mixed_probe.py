"""MIXED-PROBE task — all four capability corners in one shared-vocab stream.

Used by the LEARNABILITY study (UNIFIED_LEARNABILITY_RESULTS.md): when a single
unified cell is trained on a MIXTURE of all four probes at once, do its per-head
(lambda, beta, gamma) knobs specialise onto distinct corners (track / count /
latch / nonlinear), or do they collapse to one compromise regime?

Each sequence in a batch is a REAL instance of one randomly chosen sub-probe,
generated by that probe's own generator (no mocks). The four probes are mapped
into DISJOINT token blocks of a single shared vocabulary, so the model must infer
the active capability from the token range and apply the right recurrence with
its shared knobs:

    s5_permutation         (track)      offset 0
    anbncn_viability       (count)      offset after s5
    iterated_nonlinear_map (nonlinear)  offset after anbncn
    flag_hold_recall       (latch)      offset after iter

Both inputs and targets of each sub-probe are shifted by that probe's base, so
each sub-probe's targets live in its own block and routing is unambiguous. The
loss mask is the sub-probe's own mask (dense for s5/anbncn/iter, final-token only
for flag-hold).
"""
import numpy as np

from .s5_permutation import S5PermutationTask
from .counting_with_comparison import AnBnCnViabilityTask
from .iterated_nonlinear_map import IteratedNonlinearMapTask
from .flag_hold_recall import FlagHoldRecallTask
from .mqar_recall import MQARRecallTask


class MixedProbeTask:
    name = 'mixed_probe'

    def __init__(self, n_keys: int = 4, include_recall: bool = True,
                 recall_vocab: int = 64):
        # The capability sub-probes (same configs as the standalone runs). E98
        # FIVE-CORNER adds the leaky-linear ASSOCIATIVE-RECALL block so the mixed
        # task demands content-addressable memory too (mixed-5), not only the four
        # exotic primitives. include_recall=False recovers the original 4-cap mix.
        self.subtasks = [
            ('track', S5PermutationTask(mode='running')),
            ('count', AnBnCnViabilityTask()),
            ('nonlin', IteratedNonlinearMapTask()),
            ('latch', FlagHoldRecallTask(n_keys=n_keys)),
        ]
        if include_recall:
            self.subtasks.append(('recall', MQARRecallTask(vocab=recall_vocab)))
        # Disjoint vocab blocks: each sub-probe gets [base, base+vocab_size).
        self.bases = []
        base = 0
        for _, t in self.subtasks:
            self.bases.append(base)
            base += t.vocab_size
        self.vocab_size = base
        self._baselines = [t.random_baseline_acc() for _, t in self.subtasks]

    def generate_batch(self, B: int, T: int, rng: np.random.Generator):
        inputs = np.zeros((B, T), dtype=np.int64)
        targets = np.zeros((B, T), dtype=np.int64)
        mask = np.zeros((B, T), dtype=bool)

        # Per-sequence random assignment to a sub-probe (robust for small B).
        assign = rng.integers(0, len(self.subtasks), size=(B,))
        cursor = 0
        for si, (_, task) in enumerate(self.subtasks):
            rows = np.where(assign == si)[0]
            if rows.size == 0:
                continue
            sub_inp, sub_tgt, sub_mask = task.generate_batch(rows.size, T, rng)
            base = self.bases[si]
            inputs[rows] = sub_inp + base
            # Only offset target tokens where they are actually supervised; the
            # unsupervised positions (mask False) keep target 0 and are ignored.
            tgt_off = sub_tgt + base
            targets[rows] = np.where(sub_mask, tgt_off, 0)
            mask[rows] = sub_mask
        return inputs, targets, mask

    def random_baseline_acc(self):
        # Mask-weighted mean of the sub-probe baselines at a reference length
        # (T=128): the dense probes (s5/anbncn/iter) contribute ~T supervised
        # positions each, flag-hold contributes 1. Reported for context only;
        # the study uses measured accuracy.
        Tref = 128
        # supervised positions per sub-probe at Tref: dense probes (s5/anbncn/iter)
        # ~Tref, latch=1 final token, recall (MQAR) ~Tref//4 query positions.
        per = {'track': Tref, 'count': Tref, 'nonlin': Tref, 'latch': 1,
               'recall': Tref // 4}
        masked_per_seq = [per[name] for name, _ in self.subtasks]
        w = np.array(masked_per_seq, dtype=np.float64)
        w = w / w.sum()
        return float(np.dot(w, self._baselines))
